//show relay status
void ShowRelayStatus(uint8_t n,uint8_t status)
{		
		if(status == RC_STATUS_ON)
		{
			//'O'
			OLED_DrawBMP(64,3,79,6,KEY_FONT[12]);
			//'N'
			OLED_DrawBMP(80,3,95,6,KEY_FONT[13]);
			//Sapce
			OLED_DrawBMP(96,3,111,6,KEY_FONT[10]);			
		}
		else if(status == RC_STATUS_OFF)
		{
			//'O'
			OLED_DrawBMP(64,3,79,6,KEY_FONT[12]);
			//'F'
			OLED_DrawBMP(80,3,95,6,KEY_FONT[14]);
			//'F'
			OLED_DrawBMP(96,3,111,6,KEY_FONT[14]);			
		}
		
		if(n > 0 && n < 10)
		{
			//'K'
			OLED_DrawBMP(5,3,20,6,KEY_FONT[11]);
			//NUBER
			OLED_DrawBMP(24,3,39,6,KEY_FONT[n]);
		}
		else if(n == 10)
		{
			//'K'
			OLED_DrawBMP(5,3,20,6,KEY_FONT[11]);
			//10
			OLED_DrawBMP(24,3,41,6,KEY_FONT[1]);
			OLED_DrawBMP(42,3,59,6,KEY_FONT[0]);
		}
		else if(n == 0)
		{
			//'A'
			OLED_DrawBMP(5,3,20,6,KEY_FONT[15]);
			//'L'
			OLED_DrawBMP(21,3,36,6,KEY_FONT[16]);
			//'L'
			OLED_DrawBMP(37,3,52,6,KEY_FONT[16]);	
		}
}

